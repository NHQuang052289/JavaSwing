    void updateRoom(RoomClass r) {
        r = new RoomClass();
        System.out.println(editIndex);
        
        String roomEdit = edit.getTxtRoom().getText();
        String nameBossEdit = edit.getTxtNameBoss().getText();
        String phoneBossEdit = edit.getTxtPhoneBoss().getText();
        
        r.setRoom(roomEdit);
        r.setNameBoss(nameBossEdit);
        r.setPhoneBoss(phoneBossEdit);
        
        if(!listNew.isEmpty())
        {
            daoRoom.editRoom(listNew.get(editIndex).getRoom(), r);
        }else{
            daoRoom.editRoom(list.get(editIndex).getRoom(), r);
        }        
        list.clear();
        list = new RoomDAO().getListRoom();
        model.setRowCount(0);
        showTable();
    }
