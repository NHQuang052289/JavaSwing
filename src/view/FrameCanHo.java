    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        RoomClass r = new RoomClass();
        
        r.setRoom(txtRoom.getText());
        r.setNameBoss(txtNameBoss.getText());
        r.setPhoneBoss(txtPhoneBoss.getText());
        
        if(new RoomDAO().addRoom(r)){
            JOptionPane.showMessageDialog(rootPane, "Add room success!");
            list.add(r);
            showResult();
        }else{
            JOptionPane.showMessageDialog(rootPane, "This room already exists!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

